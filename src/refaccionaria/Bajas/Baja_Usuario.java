    public void RellenotxtUsuario() {
        String nombre = textFielda1.getText();
        System.out.println("" + nombre);
        String sql = "Select * from usuarios where nombre_usuario='" + nombre + "'";
        Statement st;
        Conexion con = new Conexion();
        Connection conexion = con.ConectarBD();

        System.out.println(sql);
        try {
            st = conexion.createStatement();
            ResultSet rs = st.executeQuery(sql);
            System.out.println(sql);
            int x = 0;

            while (rs.next()) {
                textFielda3.setText(rs.getString(1));
                textFielda2.setText(rs.getString(3));
                x = 1;
            }
            if (x == 0) {
                JOptionPane.showMessageDialog(null, "No se encontro usuario");
                LimpiarUsuarioTxt();
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error" + e.toString());
        }
    }

